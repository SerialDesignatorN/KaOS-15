function Closetestwindow() {
  var x = document.getElementById("window");
  if (x.style.display === "none") {
    x.style.display = "block";
    var files = document.getElementById("files"); files.src = ("medias/");
  } else {
    x.style.display = "none";
    var files = document.getElementById("files"); files.src = ("medias/");
  }
}
